# 标识符：
# 1.标识符中可以有数字、字母、下划线，但是不能以数字开头
# 2.标识符不能是python中的关键字和保留字，也不建议使用python中的函数名作为标识符，因为会导致函数被覆盖
# 3.命名规范：
#       在python中注意遵循两种命名规范：
#            下划线命名法：所有字母小写，单词之间使用_分割
#            帕斯卡命名法（大驼峰命名法）：首字母大写，每个单词开头字母大写，其余字母小写
